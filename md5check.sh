# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo find grep md5sum";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	FILE_LIST="$(find ./ -type f | grep 'md5$')";
	for i in ${FILE_LIST};
	do
		echo "check ${i}";
		md5sum -c "${i}";
	done


	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
