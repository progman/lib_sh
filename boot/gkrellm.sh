# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo xrandr grep awk sed";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	local WIDTH;
	local X;
	local Y;


	WIDTH=$(xrandr -q | grep '\*' | awk '{ print $1 }' | sed -e 's/x.*//g');
#1920-1852=68


	X=1852;
	Y=128;
	if [ "${FLAG_GKRELLM_DOWN}" == "1" ];
	then
		Y=191;
	fi


	gkrellm --geometry "+${X}+${Y}"


	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
