# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
	if [ "${1}" == "" ];
	then
		echo "example: ${0} FILE";
		return 1;
	fi


# check depends tools
	check_prog "echo mplayer";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


# convert
	mplayer -vo null -ao pcm:file="${1}.wav" "${1}" &> /dev/null < /dev/null;


	return "${?}";
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
