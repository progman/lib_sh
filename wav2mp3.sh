# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
# check depends tools
	check_prog "echo cat lame mktemp";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	if [ "${1}" == "-h" ] || [ "${1}" == "-help" ] || [ "${1}" == "--help" ];
	then
		echo "example: ls -1 *.wav | ${0}";
		return 1;
	fi


	local TMP;
	TMP=$(mktemp);


	while read -r FILE;
	do
		echo "${FILE}" >> "${TMP}";
	done


	local COUNT_CUR=1;
	local COUNT_ALL;
	COUNT_ALL=$(wc -l "${TMP}" | { read a b; echo ${a}; });


# convert
	while read -r FILE;
	do
		if [ -e "${FILE}" ];
		then
			echo "[${COUNT_CUR}/${COUNT_ALL}] ${FILE}";
			lame --quiet -h -q 0 -v -V 0 -B 320 "${FILE}" "${FILE}.mp3" < /dev/null;
		fi

		(( COUNT_CUR++ ));

	done < "${TMP}";


	rm -- -rf "${TMP}" &> /dev/null;


	return "${?}";
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
