# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
	if [ "${1}" == "" ];
	then
		echo "example: ${0} FILE.WAV";
		return 1;
	fi


# check depends tools
	check_prog "echo lame";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


# convert
	lame -h -q 0 -v -V 3 -B 320 "${1}" "${1}.mp3" < /dev/null;


	return "${?}";
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
