# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
	if [ "${1}" == "" ] || [ ! -e "${1}" ];
	then
		echo "example: ${0} FILE";
		return 1;
	fi


# check depends tools
	check_prog "echo md5sum awk sed mv";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


	HASH=$(md5sum "${1}" | awk '{print $1}');

#	EXT=$(echo "${1}" | sed -e 's/^[^\.]*//g');
	EXT=$(echo "${1}" | sed -e 's/.*\.//g');

	if [ "${1}" == "${EXT}" ];
	then
		EXT="";
	fi

	NEW="${HASH}";
	if [ "${EXT}" != "" ];
	then
		NEW="${HASH}.${EXT}";
	fi

	echo "${NEW}";

	if [ -e "${NEW}" ];
	then
		return 2;
	fi

	mv "${1}" "${NEW}";
	if [ "${?}" != "0" ];
	then
		return 3;
	fi

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
