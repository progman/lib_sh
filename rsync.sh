# check depends
function check_prog()
{
	for i in ${1};
	do
		if [ "$(which ${i})" == "" ];
		then
			echo "FATAL: you must install \"${i}\"...";
			return 1;
		fi
	done

	return 0;
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
# general function
function main()
{
	if [ ! -d "${1}" ] || [ ! -d "${2}" ];
	then
		echo "example: ${0} SOURCE_DIR TARGET_DIR";
		return 1;
	fi


# check depends tools
	check_prog "echo ionice kill nice rsync";
	if [ "${?}" != "0" ];
	then
		return 1;
	fi


# check TMPDIR
	if [ "${TMPDIR}" == "" ];
	then
		TMPDIR='/tmp/';
	fi


# check flag disable
	if [ -e "${TMPDIR}/FLAG_RSYNC_DISABLE" ];
	then
		return 0;
	fi


# check race condition
	if [ "${RSYNC_PIDFILE}" == "" ];
	then
		RSYNC_PIDFILE="/var/run/rsync.pid";
	fi

	if [ -e "${RSYNC_PIDFILE}" ];
	then
		PID="$(cat ${RSYNC_PIDFILE})";

		kill -0 "${PID}" &> /dev/null;
		if [ "${?}" == "0" ];
		then
			return 0; # program already run
		fi
	fi
	echo "${BASHPID}" > "${RSYNC_PIDFILE}";

#	ionice -c 3 nice -n 19 rsync -azLv --safe-links
	ionice -c 3 nice -n 19 rsync -av --delete "${1}" "${2}";

	return "${?}";
}
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
main "${@}";

exit "${?}";
